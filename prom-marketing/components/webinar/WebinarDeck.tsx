export function WebinarDeck() {
  const [i, setI] = useState(0);
  const [showNotes, setShowNotes] = useState(false);
  const [startedAt, setStartedAt] = useState<number | null>(null);
  const [elapsed, setElapsed] = useState(0);
  const slide = SLIDES[i];
  const total = SLIDES.length;

  const go = useCallback(
    (delta: number) => {
      setI((cur) => Math.min(total - 1, Math.max(0, cur + delta)));
      setStartedAt((s) => s ?? Date.now());
    },
    [total],
  );

  useEffect(() => {
    function onKey(e: KeyboardEvent) {
      if (e.key === "ArrowRight" || e.key === " " || e.key === "PageDown") go(1);
      else if (e.key === "ArrowLeft" || e.key === "PageUp") go(-1);
      else if (e.key.toLowerCase() === "n") setShowNotes((v) => !v);
      else if (e.key.toLowerCase() === "f") {
        if (document.fullscreenElement) void document.exitFullscreen();
        else void document.documentElement.requestFullscreen();
      }
    }
    window.addEventListener("keydown", onKey);
    return () => window.removeEventListener("keydown", onKey);
  }, [go]);

  useEffect(() => {
    if (!startedAt) return;
    const t = setInterval(() => setElapsed(Date.now() - startedAt), 1000);
    return () => clearInterval(t);
  }, [startedAt]);

  const timer = useMemo(() => {
    const s = Math.floor(elapsed / 1000);
    return `${String(Math.floor(s / 60)).padStart(2, "0")}:${String(s % 60).padStart(2, "0")}`;
  }, [elapsed]);

  const accentBg =
    slide.accent === "cyan"
      ? "radial-gradient(100vw 80vh at 50% 30%, rgba(34,211,238,0.22), transparent 65%)"
      : slide.accent === "violet"
        ? "radial-gradient(100vw 80vh at 50% 30%, rgba(124,58,237,0.28), transparent 65%)"
        : "radial-gradient(90vw 70vh at 30% 20%, rgba(34,211,238,0.10), transparent 60%), radial-gradient(80vw 60vh at 85% 85%, rgba(124,58,237,0.12), transparent 60%)";

  return (
    <div
      className="relative flex min-h-screen select-none flex-col bg-[var(--color-bg-void)] text-white"
      onClick={(e) => {
        // клик в дясната половина = напред, лявата = назад
        const x = e.clientX / window.innerWidth;
        go(x > 0.3 ? 1 : -1);
      }}
    >
      <div aria-hidden className="pointer-events-none absolute inset-0" style={{ background: accentBg }} />

      {/* Горна лента */}
      <div className="relative z-10 flex items-center justify-between px-8 pt-6 font-mono text-xs uppercase tracking-[0.22em] text-slate-500">
        <span>ProMarketing · {WEBINAR.title}</span>
        <span className="flex items-center gap-6">
          <span>{timer}</span>
          <span className="text-cyan-300">
            {i + 1} / {total}
          </span>
        </span>
      </div>

      {/* Слайд */}
      <div className="relative z-10 mx-auto flex w-full max-w-5xl flex-1 flex-col justify-center px-8 py-12">
        {slide.kicker && (
          <p className="mb-5 font-mono text-sm uppercase tracking-[0.3em] text-cyan-300">{slide.kicker}</p>
        )}
        <h1 className="text-[clamp(30px,4.6vw,58px)] font-bold leading-[1.08] tracking-tight">{slide.title}</h1>

        {slide.big && (
          <p className="mt-8 max-w-3xl text-[clamp(20px,2.6vw,32px)] font-light leading-snug text-slate-200">
            {slide.big}
          </p>
        )}

        {slide.bullets && (
          <ul className="mt-9 max-w-3xl space-y-4">
            {slide.bullets.map((b) => (
              <li key={b} className="flex items-start gap-4 text-[clamp(17px,2vw,24px)] text-slate-200">
                <span className="mt-[0.55em] h-2 w-2 shrink-0 rounded-full bg-cyan-300 shadow-[0_0_12px_rgba(34,211,238,0.9)]" />
                {b}
              </li>
            ))}
          </ul>
        )}

        {slide.stack && (
          <div className="mt-9 w-full max-w-2xl overflow-hidden rounded-2xl border border-white/10">
            {slide.stack.map(([label, value], idx) => (
              <div
                key={label}
                className={`flex items-center justify-between gap-6 px-6 py-4 text-[clamp(15px,1.6vw,20px)] ${idx % 2 ? "bg-white/[0.02]" : "bg-white/[0.05]"}`}
              >
                <span className="text-slate-200">{label}</span>
                <span className="shrink-0 font-mono font-bold text-cyan-300">{value}</span>
              </div>
            ))}
          </div>
        )}
      </div>

      {/* Прогрес */}
      <div className="relative z-10 h-1 w-full bg-white/5">
        <div
          className="h-full bg-[var(--color-accent-cyan)] shadow-[0_0_12px_rgba(34,211,238,0.8)] transition-all duration-300"
          style={{ width: `${((i + 1) / total) * 100}%` }}
        />
      </div>

      {/* Бележки за говорителя (N) */}
      {showNotes && (
        <div
          className="relative z-10 max-h-[32vh] overflow-y-auto border-t border-amber-400/30 bg-[rgba(30,20,0,0.92)] px-8 py-5 text-[15px] leading-relaxed text-amber-100 backdrop-blur"
          onClick={(e) => e.stopPropagation()}
        >
          <p className="mb-1 font-mono text-[10px] uppercase tracking-[0.3em] text-amber-400">
            Бележки за говорителя (N скрива) · → напред · ← назад · F цял екран
          </p>
          {slide.notes}
        </div>
      )}

      {!showNotes && (
        <p className="relative z-10 px-8 pb-4 text-center font-mono text-[10px] uppercase tracking-[0.25em] text-slate-700">
          → / клик = напред · ← = назад · N = бележки · F = цял екран
        </p>
      )}
    </div>
  );
}
