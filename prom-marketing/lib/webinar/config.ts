/** Форматира дата за показване (бг локал). */
export function webinarDateLabel(): string | null {
  if (WEBINAR.dateLabel) return WEBINAR.dateLabel;
  if (!WEBINAR.dateISO) return null;
  const d = new Date(WEBINAR.dateISO);
  return new Intl.DateTimeFormat("bg-BG", {
    weekday: "long",
    day: "numeric",
    month: "long",
    hour: "2-digit",
    minute: "2-digit",
    timeZone: "Europe/Sofia",
  }).format(d);
}
